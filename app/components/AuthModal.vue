  <div
    v-if="isOpen"
    class="fixed inset-0 z-50 flex items-center justify-center bg-black/50 backdrop-blur-sm"
    @click.self="closeModal"
  >
    <div class="relative w-full max-w-md rounded-lg bg-white p-8 shadow-2xl dark:bg-gray-800">
      <!-- Close button -->
      <button
        class="absolute right-4 top-4 text-gray-400 hover:text-gray-600 dark:hover:text-gray-200"
        @click="closeModal"
      >
        <svg
          class="h-6 w-6"
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
        >
          <path
            stroke-linecap="round"
            stroke-linejoin="round"
            stroke-width="2"
            d="M6 18L18 6M6 6l12 12"
          />
        </svg>
      </button>

      <!-- Header -->
      <h2 class="mb-6 text-2xl font-bold text-gray-900 dark:text-white">
        {{ mode === 'login' ? 'Sign In' : 'Create Account' }}
      </h2>

      <!-- Error message -->
      <div
        v-if="error"
        class="mb-4 rounded-lg bg-red-50 p-3 text-sm text-red-800 dark:bg-red-900/20 dark:text-red-200"
      >
        {{ error }}
      </div>

      <!-- Social login buttons -->
      <div class="mb-6 space-y-3">
        <a
          href="/api/auth/google"
          class="flex w-full items-center justify-center gap-2 rounded-lg border border-gray-300 bg-white px-4 py-2 font-semibold text-gray-700 hover:bg-gray-50 dark:border-gray-600 dark:bg-gray-700 dark:text-white dark:hover:bg-gray-600"
        >
          <svg class="h-5 w-5" viewBox="0 0 24 24">
            <path
              fill="#4285F4"
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
            />
            <path
              fill="#34A853"
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            />
            <path
              fill="#FBBC05"
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            />
            <path
              fill="#EA4335"
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            />
          </svg>
          Continue with Google
        </a>
        <a
          href="/api/auth/github"
          class="flex w-full items-center justify-center gap-2 rounded-lg border border-gray-300 bg-gray-900 px-4 py-2 font-semibold text-white hover:bg-gray-800 dark:border-gray-600"
        >
          <svg class="h-5 w-5" fill="currentColor" viewBox="0 0 24 24">
            <path
              fill-rule="evenodd"
              d="M12 2C6.477 2 2 6.484 2 12.017c0 4.425 2.865 8.18 6.839 9.504.5.092.682-.217.682-.483 0-.237-.008-.868-.013-1.703-2.782.605-3.369-1.343-3.369-1.343-.454-1.158-1.11-1.466-1.11-1.466-.908-.62.069-.608.069-.608 1.003.07 1.531 1.032 1.531 1.032.892 1.53 2.341 1.088 2.91.832.092-.647.35-1.088.636-1.338-2.22-.253-4.555-1.113-4.555-4.951 0-1.093.39-1.988 1.029-2.688-.103-.253-.446-1.272.098-2.65 0 0 .84-.27 2.75 1.026A9.564 9.564 0 0112 6.844c.85.004 1.705.115 2.504.337 1.909-1.296 2.747-1.027 2.747-1.027.546 1.379.202 2.398.1 2.651.64.7 1.028 1.595 1.028 2.688 0 3.848-2.339 4.695-4.566 4.943.359.309.678.92.678 1.855 0 1.338-.012 2.419-.012 2.747 0 .268.18.58.688.482A10.019 10.019 0 0022 12.017C22 6.484 17.522 2 12 2z"
              clip-rule="evenodd"
            />
          </svg>
          Continue with GitHub
        </a>
      </div>

      <!-- Divider -->
      <div class="relative mb-6">
        <div class="absolute inset-0 flex items-center">
          <div class="w-full border-t border-gray-300 dark:border-gray-600" />
        </div>
        <div class="relative flex justify-center text-sm">
          <span class="bg-white px-2 text-gray-500 dark:bg-gray-800 dark:text-gray-400">Or continue with email</span>
        </div>
      </div>

      <!-- Login form -->
      <form
        v-if="mode === 'login'"
        @submit.prevent="handleLogin"
      >
        <div class="mb-4">
          <label class="mb-2 block text-sm font-medium text-gray-700 dark:text-gray-300">Email</label>
          <input
            v-model="loginForm.email"
            type="email"
            required
            class="w-full rounded-lg border border-gray-300 px-4 py-2 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
            placeholder="your@email.com"
          >
        </div>
        <div class="mb-6">
          <label class="mb-2 block text-sm font-medium text-gray-700 dark:text-gray-300">Password</label>
          <input
            v-model="loginForm.password"
            type="password"
            required
            class="w-full rounded-lg border border-gray-300 px-4 py-2 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
            placeholder="••••••••"
          >
          <div class="mt-2 flex items-center justify-between">
            <label class="flex items-center cursor-pointer">
              <input
                v-model="loginForm.rememberMe"
                type="checkbox"
                class="h-4 w-4 rounded border-gray-300 text-primary-600 focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700"
              >
              <span class="ml-2 text-sm text-gray-700 dark:text-gray-300">Remember me</span>
            </label>
            <NuxtLink
              to="/forgot-password"
              class="text-sm text-primary-600 hover:text-primary-700 dark:text-primary-400"
              @click="closeModal"
            >
              Forgot password?
            </NuxtLink>
          </div>
        </div>
        <button
          type="submit"
          :disabled="isLoading"
          class="mb-4 w-full rounded-lg bg-primary-600 px-4 py-2 font-semibold text-white hover:bg-primary-700 disabled:cursor-not-allowed disabled:opacity-50"
        >
          {{ isLoading ? 'Signing in...' : 'Sign In' }}
        </button>
      </form>

      <!-- Register form -->
      <form
        v-else
        @submit.prevent="handleRegister"
      >
        <div class="mb-4">
          <label class="mb-2 block text-sm font-medium text-gray-700 dark:text-gray-300">Email</label>
          <input
            v-model="registerForm.email"
            type="email"
            required
            class="w-full rounded-lg border border-gray-300 px-4 py-2 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
            placeholder="your@email.com"
          >
        </div>
        <div class="mb-4">
          <label class="mb-2 block text-sm font-medium text-gray-700 dark:text-gray-300">Username</label>
          <input
            v-model="registerForm.username"
            type="text"
            required
            class="w-full rounded-lg border border-gray-300 px-4 py-2 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
            placeholder="username"
          >
        </div>
        <div class="mb-6">
          <label class="mb-2 block text-sm font-medium text-gray-700 dark:text-gray-300">Password</label>
          <input
            v-model="registerForm.password"
            type="password"
            required
            class="w-full rounded-lg border border-gray-300 px-4 py-2 focus:border-primary-500 focus:outline-none focus:ring-2 focus:ring-primary-500 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
            placeholder="••••••••"
          >
          <p class="mt-1 text-xs text-gray-500 dark:text-gray-400">
            At least 8 characters, one uppercase, one lowercase, and one number
          </p>
        </div>
        <button
          type="submit"
          :disabled="isLoading"
          class="mb-4 w-full rounded-lg bg-primary-600 px-4 py-2 font-semibold text-white hover:bg-primary-700 disabled:cursor-not-allowed disabled:opacity-50"
        >
          {{ isLoading ? 'Creating account...' : 'Create Account' }}
        </button>
      </form>

      <!-- Toggle mode -->
      <div class="text-center text-sm text-gray-600 dark:text-gray-400">
        {{ mode === 'login' ? "Don't have an account?" : 'Already have an account?' }}
        <button
          class="ml-1 font-semibold text-primary-600 hover:text-primary-700 dark:text-primary-400"
          @click="toggleMode"
        >
          {{ mode === 'login' ? 'Sign up' : 'Sign in' }}
        </button>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
const props = defineProps<{
  isOpen: boolean
  initialMode?: 'login' | 'register'
}>()

const emit = defineEmits<{
  close: []
  success: []
}>()

const { login, register } = useAuth()

const mode = ref<'login' | 'register'>(props.initialMode || 'login')
const isLoading = ref(false)
const error = ref('')

const loginForm = reactive({
  email: '',
  password: '',
  rememberMe: false,
})

const registerForm = reactive({
  email: '',
  username: '',
  password: '',
})

const toggleMode = () => {
  mode.value = mode.value === 'login' ? 'register' : 'login'
  error.value = ''
}

const closeModal = () => {
  error.value = ''
  emit('close')
}

const handleLogin = async () => {
  isLoading.value = true
  error.value = ''

  const result = await login(loginForm.email, loginForm.password, loginForm.rememberMe)

  if (result.success) {
    emit('success')
    closeModal()
  }
  else {
    error.value = result.error || 'Login failed'
  }

  isLoading.value = false
}

const handleRegister = async () => {
  isLoading.value = true
  error.value = ''

  const result = await register(
    registerForm.email,
    registerForm.username,
    registerForm.password,
  )

  if (result.success) {
    emit('success')
    closeModal()
  }
  else {
    error.value = result.error || 'Registration failed'
  }

  isLoading.value = false
}

watch(() => props.isOpen, (newVal) => {
  if (newVal) {
    error.value = ''
  }
})
</script>
